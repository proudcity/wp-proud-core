    <p>Guided by Web Content Accessibility Guidelines (WCAG) standards, our objective is to be:</p>
    <ol>
      <li>Fully compliant with Section 508 and WCAG Level AA.</li>
      <li>Open and transparent about our process.</li>
      <li>Attentive to community feedback surrounding usability of our website.</li>
    </ol>
    <p>Accessibility is an ongoing process, and here are steps we have taken:</p>
    <ul>
      <li>Accessible forms: "aria" attributes, descriptive labels, tab-able interfaces</li>
      <li>Semantic markup and additional "aria" descriptors on UI components</li>
      <li>Tab-able navigational elements</li>
      <li>"Skiplinks" to important page elements and website sitemap</li>
      <li>Required "alt" descriptions on images</li>
      <li>Font size adjustment</li>
    </ul>
